CREATE OR REPLACE FUNCTION public.event_rating_summary()
RETURNS TABLE (
  event_id BIGINT,
  title TEXT,
  average_rating NUMERIC,
  total_reviews BIGINT
)
LANGUAGE sql
STABLE
AS $$
  SELECT
    e.event_id,
    e.title,
    COALESCE(round(avg(r.rating)::numeric, 1), 0) AS average_rating,
    count(r.review_id) AS total_reviews
  FROM public.events e
  LEFT JOIN public.reviews r ON r.event_id = e.event_id
  GROUP BY e.event_id, e.title
  ORDER BY e.event_id;
$$;

CREATE OR REPLACE FUNCTION public.event_sales_report()
RETURNS TABLE (
  event_id BIGINT,
  title TEXT,
  tickets_booked BIGINT,
  gross_amount NUMERIC,
  paid_amount NUMERIC
)
LANGUAGE sql
STABLE
AS $$
  SELECT
    e.event_id,
    e.title,
    COALESCE(sum(b.quantity), 0)::bigint AS tickets_booked,
    COALESCE(sum(b.quantity * e.price), 0)::numeric AS gross_amount,
    COALESCE(sum(CASE WHEN p.payment_status = 'paid' THEN p.amount ELSE 0 END), 0)::numeric AS paid_amount
  FROM public.events e
  LEFT JOIN public.bookings b ON b.event_id = e.event_id
  LEFT JOIN public.payments p ON p.booking_id = b.booking_id
  GROUP BY e.event_id, e.title
  ORDER BY e.event_id;
$$;

