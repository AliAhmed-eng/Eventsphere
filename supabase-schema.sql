CREATE TABLE public.users (
  user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL CHECK (length(btrim(name)) > 0),
  first_name TEXT,
  last_name TEXT,
  email TEXT NOT NULL UNIQUE CHECK (email ~* '^[^[:space:]@]+@[^[:space:]@]+\.[^[:space:]@]+$'),
  phone TEXT NOT NULL CHECK (length(btrim(phone)) >= 7),
  password TEXT NOT NULL CHECK (length(password) >= 6),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE public.categories (
  category_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category_name TEXT NOT NULL UNIQUE CHECK (length(btrim(category_name)) > 0),
  name TEXT UNIQUE,
  description TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE public.organizers (
  organizer_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  organizer_name TEXT NOT NULL CHECK (length(btrim(organizer_name)) > 0),
  name TEXT,
  email TEXT NOT NULL UNIQUE CHECK (email ~* '^[^[:space:]@]+@[^[:space:]@]+\.[^[:space:]@]+$'),
  phone TEXT NOT NULL CHECK (length(btrim(phone)) >= 7),
  company TEXT,
  bio TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (organizer_name, email)
);

CREATE TABLE public.events (
  event_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL CHECK (length(btrim(title)) > 0),
  venue TEXT NOT NULL CHECK (length(btrim(venue)) > 0),
  event_date TIMESTAMPTZ NOT NULL,
  price NUMERIC(10, 2) NOT NULL DEFAULT 0 CHECK (price >= 0),
  available_seats INTEGER NOT NULL DEFAULT 0 CHECK (available_seats >= 0),
  description TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (title, venue, event_date)
);

CREATE TABLE public.event_categories (
  event_category_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  event_id BIGINT NOT NULL REFERENCES public.events(event_id) ON DELETE CASCADE,
  category_id BIGINT NOT NULL REFERENCES public.categories(category_id) ON DELETE CASCADE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (event_id, category_id)
);

CREATE TABLE public.event_organizers (
  event_organizer_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  event_id BIGINT NOT NULL REFERENCES public.events(event_id) ON DELETE CASCADE,
  organizer_id BIGINT NOT NULL REFERENCES public.organizers(organizer_id) ON DELETE CASCADE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (event_id, organizer_id)
);

CREATE TABLE public.bookings (
  booking_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL REFERENCES public.users(user_id) ON DELETE CASCADE,
  event_id BIGINT NOT NULL REFERENCES public.events(event_id) ON DELETE CASCADE,
  quantity INTEGER NOT NULL DEFAULT 1 CHECK (quantity > 0),
  num_tickets INTEGER NOT NULL DEFAULT 1 CHECK (num_tickets > 0),
  booking_date TIMESTAMPTZ NOT NULL DEFAULT now(),
  CHECK (quantity = num_tickets)
);

CREATE TABLE public.wishlist (
  wishlist_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL REFERENCES public.users(user_id) ON DELETE CASCADE,
  event_id BIGINT NOT NULL REFERENCES public.events(event_id) ON DELETE CASCADE,
  added_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (user_id, event_id)
);

CREATE TABLE public.reviews (
  review_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL REFERENCES public.users(user_id) ON DELETE CASCADE,
  event_id BIGINT NOT NULL REFERENCES public.events(event_id) ON DELETE CASCADE,
  rating INTEGER NOT NULL CHECK (rating BETWEEN 1 AND 5),
  comment TEXT NOT NULL DEFAULT '' CHECK (length(comment) <= 500),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (user_id, event_id)
);

CREATE TABLE public.tickets (
  ticket_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_id BIGINT NOT NULL REFERENCES public.bookings(booking_id) ON DELETE CASCADE,
  event_id BIGINT NOT NULL REFERENCES public.events(event_id) ON DELETE CASCADE,
  seat_number INTEGER NOT NULL CHECK (seat_number > 0),
  qr_code TEXT NOT NULL UNIQUE CHECK (length(btrim(qr_code)) > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (event_id, seat_number)
);

CREATE TABLE public.payments (
  payment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_id BIGINT NOT NULL UNIQUE REFERENCES public.bookings(booking_id) ON DELETE CASCADE,
  amount NUMERIC(10, 2) NOT NULL CHECK (amount >= 0),
  payment_status TEXT NOT NULL DEFAULT 'pending' CHECK (payment_status IN ('pending', 'paid', 'failed')),
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE OR REPLACE FUNCTION public.sync_user_name()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF NULLIF(btrim(NEW.name), '') IS NULL THEN
    NEW.name := btrim(concat_ws(' ', NULLIF(NEW.first_name, ''), NULLIF(NEW.last_name, '')));
  END IF;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.sync_category_name()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF NULLIF(btrim(NEW.category_name), '') IS NULL THEN
    NEW.category_name := NEW.name;
  END IF;
  IF NULLIF(btrim(NEW.name), '') IS NULL THEN
    NEW.name := NEW.category_name;
  END IF;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.sync_organizer_name()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF NULLIF(btrim(NEW.organizer_name), '') IS NULL THEN
    NEW.organizer_name := NEW.name;
  END IF;
  IF NULLIF(btrim(NEW.name), '') IS NULL THEN
    NEW.name := NEW.organizer_name;
  END IF;
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_sync_user_name
BEFORE INSERT OR UPDATE ON public.users
FOR EACH ROW EXECUTE FUNCTION public.sync_user_name();

CREATE TRIGGER trg_sync_category_name
BEFORE INSERT OR UPDATE ON public.categories
FOR EACH ROW EXECUTE FUNCTION public.sync_category_name();

CREATE TRIGGER trg_sync_organizer_name
BEFORE INSERT OR UPDATE ON public.organizers
FOR EACH ROW EXECUTE FUNCTION public.sync_organizer_name();

